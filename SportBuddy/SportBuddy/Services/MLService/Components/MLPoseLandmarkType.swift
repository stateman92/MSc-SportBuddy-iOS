/// The types of the landmark.
enum MLPoseLandmarkType: String {
    /// Nose.
    case nose

    /// The inner edge of the left eye.
    case leftEyeInner

    /// The center of the left eye.
    case leftEye

    /// The outer edge of the left eye.
    case leftEyeOuter

    /// The inner edge of the right eye.
    case rightEyeInner

    /// The center of the right eye.
    case rightEye

    /// The outer edge of the right eye.
    case rightEyeOuter

    /// The left ear.
    case leftEar

    /// The right ear.
    case rightEar

    /// The left edge of the mouth.
    case mouthLeft

    /// The right edge of the mouth.
    case mouthRight

    /// The left shoulder.
    case leftShoulder

    /// The right shoulder.
    case rightShoulder

    /// The left elbow.
    case leftElbow

    /// The right elbow.
    case rightElbow

    /// The left wrist.
    case leftWrist

    /// The right wrist.
    case rightWrist

    /// The left pinky finger.
    case leftPinkyFinger

    /// The right pinky finger.
    case rightPinkyFinger

    /// The left index finger.
    case leftIndexFinger

    /// The right index finger.
    case rightIndexFinger

    /// The left thumb.
    case leftThumb

    /// The right thumb.
    case rightThumb

    /// The left hip.
    case leftHip

    /// The right hip.
    case rightHip

    /// The left knee.
    case leftKnee

    /// The right knee.
    case rightKnee

    /// The left ankle.
    case leftAnkle

    /// The right ankle.
    case rightAnkle

    /// The left heel.
    case leftHeel

    /// The right heel.
    case rightHeel

    /// The left toe.
    case leftToe

    /// The right toe.
    case rightToe
}
